defmodule TaskManagerWeb.UsersController do
  use TaskManagerWeb, :controller
  use TaskManagerWeb.ApiDoc.UserDoc

  alias TaskManager.Users
  alias TaskManager.Users.User

  action_fallback TaskManagerWeb.FallbackController

  def index(conn, params) do
    %{entries: users, metadata: pagination} = Users.list_users(params)
    render(conn, :index, users: users, pagination: pagination)
  end

  def create(conn, %{"user" => user_params}) do
    with {:ok, %User{} = user} <- Users.create_user(user_params) do
      conn
      |> put_status(:created)
      |> render(:show, user: user)
    end
  end

  def show(conn, %{"id" => id}) do
    user = Users.get_user!(id)
    render(conn, :show, user: user)
  end

  def update(conn, %{"id" => id, "user" => user_params}) do
    user = Users.get_user!(id)

    with {:ok, %User{} = user} <- Users.update_user(user, user_params) do
      render(conn, :show, user: user)
    end
  end

  def delete(conn, %{"id" => id}) do
    user = Users.get_user!(id)

    with {:ok, %User{}} <- Users.delete_user(user) do
      send_resp(conn, :no_content, "")
    end
  end
end
